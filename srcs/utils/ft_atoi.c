#include "cub3d.h"

int	ft_atoi(char *str)
{
	int	nb;

	nb = 0;
	while (*str <= ' ')
		str++;
	while (*str >= '0' && *str <= '9')
	{
		nb = nb * 10 + *str - '0';
		str++;
	}
	return (nb);
}
